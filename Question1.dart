//Create two integer variables length and breadth and assign values
// then check if they are square values or rectangle values.
//i.e: if both values are equal then it's square otherwise rectangle.
void main() {
  int length = 5;
  int breadth = 9;

  if (length == breadth) {
    print('The given values represent a square.');
  } else {
    print('The given values represent a rectangle.');
  }
}
